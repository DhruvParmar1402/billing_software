    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        String id1=txtprodid.getText();
        String n1=txtprodname.getText();
        String  p1=txtprodprice.getText();
        String d1=txtproddescription.getText();
        String quant=txtprodquantity.getText();
        if(lib.check(new String[] {id1,n1,p1,d1,d1,quant})==false)
        {
            JOptionPane.showConfirmDialog(this,"empty fields","error",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
        try
        {
                con=lib.getcon(this);
                st=con.createStatement();
                int no=st.executeUpdate("update new_product set prod_name='"+n1+"',prod_price="+p1+",description='"+d1+"',quantity="+Integer.parseInt(quant)+" where prod_id="+id1+"");
                JOptionPane.showConfirmDialog(this,""+no+" record(s) updated","updated",JOptionPane.OK_OPTION); 
                if(no>=0)
                {
                    txtprodid.setText("");
                    txtprodname.setText("");
                    txtprodprice.setText("");
                    txtproddescription.setText("");
                    txtprodquantity.setText("");
                    txtprodid.setEditable(true);
                }
        }
        catch(Exception e)
        {
            
        }
        }
    }//GEN-LAST:event_btnupdateActionPerformed
