    private void searchprodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchprodActionPerformed
        // TODO add your handling code here:
        // search button
        int pid=Integer.parseInt(txtprodid.getText());
          try
            {
                con=lib.getcon(this);
                st=con.createStatement();
                rs=st.executeQuery("select * from new_product where prod_id='"+pid+"'");
                if(rs.next())
                {
                    txtprodid.setEditable(false);
                    txtprodname.setText(rs.getString(2));
                    txtprodprice.setText(rs.getString(3));
                    txtproddescription.setText(rs.getString(4));
                    txtprodquantity.setText(rs.getString(5));
                    
                }
                else 
                {
                     JOptionPane.showConfirmDialog(this,"No such number was found","error",JOptionPane.OK_OPTION); 
                }
            }      
            catch(Exception e)
            {
                
            }
    }//GEN-LAST:event_searchprodActionPerformed
