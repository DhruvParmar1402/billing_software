    private void btnsearchbuyerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchbuyerActionPerformed
        // TODO add your handling code here:
        String contactno=btncontactno.getText();
         if(lib.check(new String[]{contactno})==false)
            JOptionPane.showConfirmDialog(this,"empty fields","error",JOptionPane.ERROR_MESSAGE);
         
         
          else
          {
              long a=Long.parseLong(btncontactno.getText());
               try
               {
                 con=lib.getcon(this);
                 st=con.createStatement();
                 rs=st.executeQuery("select * from new_buyer where contactno='"+a+"'");
                 if(rs.next())
                 {
                    btncontactno.setEditable(false);
                    txtupdatename.setText(rs.getString(1));
                   // jTextField3.setText(rs.getString(2));
                    txtupdateemail.setText(rs.getString(3));
                    txtupdateadd.setText(rs.getString(4));
                    combodender.setSelectedItem(rs.getString(5));
                 }
                 else 
                 {
                     JOptionPane.showConfirmDialog(this,"No such number was found","error",JOptionPane.OK_OPTION); 
                 }
                }      
                catch(Exception e)
                {
                
                }
          }
    }//GEN-LAST:event_btnsearchbuyerActionPerformed

