    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        
        String n1=txtupdatename.getText();
        String contactno=btncontactno.getText();
        String e1=txtupdateemail.getText();
        String a1=txtupdateadd.getText();
        String g1=(String)combodender.getSelectedItem();
        
        
       String regexemail1="[a-z][a-z0-9._]*@[a-z0-9]+[.][a-z0-9.]*" ;
       String regexname="[a-zA-Z]+[\\s][a-zA-Z]*";
     
      
       Pattern pattern2 = Pattern.compile(regexname);
       Pattern pattern3=Pattern.compile(regexemail1);
       
         Matcher matcher3=pattern3.matcher(e1);
         Matcher matcher2=pattern2.matcher(n1);
      
 
       
       
        if(lib.check(new String[]{n1,contactno,e1,a1,g1})==false)
            JOptionPane.showConfirmDialog(this,"empty fields","error",JOptionPane.ERROR_MESSAGE);
        else if (matcher2.matches()&&matcher3.matches() ){
             Long c1=Long.parseLong(btncontactno.getText()); 
            try
            {
                con=lib.getcon(this);
                st=con.createStatement();
                int no=st.executeUpdate("update new_buyer set name='"+n1+"',contactno="+c1+",email='"+e1+"',address='"+a1+"',gender='"+g1+"' where contactno="+c1+"");
                JOptionPane.showConfirmDialog(this,""+no+" record(s) updated","updated",JOptionPane.OK_OPTION); 
                if(no>=0)
                {
                    btncontactno.setText("");
                    txtupdatename.setText("");
                    txtupdateemail.setText("");
                    txtupdateadd.setText("");
                    btncontactno.setEditable(true);
                }
            }
            catch(Exception e)
            {
            
            }
        }
        else
            JOptionPane.showConfirmDialog(this,"inncorrect fields","error",JOptionPane.ERROR_MESSAGE);
       
    }//GEN-LAST:event_btnupdateActionPerformed

